--Query type: DQL
WITH DateValues AS (
    SELECT CAST('2019-03-01' AS DATE) AS Date1, CAST('2019-02-15' AS DATE) AS Date2
    UNION ALL
    SELECT CAST('2019-03-01 02:00:00' AS DATETIME), CAST('2019-02-15 01:00:00' AS DATETIME)
    UNION ALL
    SELECT CAST('2019-02-15 02:00:00' AS DATETIME), CAST('2019-02-15 01:00:00' AS DATETIME)
)
SELECT DATEDIFF(MONTH, Date2, Date1) AS MonthsBetween
FROM DateValues
