--Query type: DQL
WITH sales_data AS (
    SELECT CAST('2022-01-01' AS DATE) AS sale_date, 250000 AS price
    UNION ALL
    SELECT CAST('2022-01-02' AS DATE) AS sale_date, 350000 AS price
    UNION ALL
    SELECT CAST('2022-01-03' AS DATE) AS sale_date, 450000 AS price
    UNION ALL
    SELECT CAST('2022-01-04' AS DATE) AS sale_date, 550000 AS price
)
SELECT sale_date, price, CASE
    WHEN price BETWEEN 200000 AND 300000 THEN '200000-300000'
    WHEN price BETWEEN 300001 AND 400000 THEN '300001-400000'
    WHEN price BETWEEN 400001 AND 500000 THEN '400001-500000'
    WHEN price BETWEEN 500001 AND 600000 THEN '500001-600000'
END AS [PRICE GROUP]
FROM sales_data
ORDER BY sale_date;